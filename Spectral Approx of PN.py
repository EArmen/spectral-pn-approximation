def write_results_csv(results, outpath):
    fieldnames = list(asdict(results[0]).keys())
    with open(outpath, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for r in results:
            writer.writerow(asdict(r))


def make_latex_table(results):
    lines = []
    lines.append(r"\begin{table}[htbp]")
    lines.append(r"\centering")
    lines.append(
        r"\caption{Numerical resolution diagnostics for the finite-interval equilibrium problem as "
        r"\(\delta=L_D/L\) decreases. The primary theorem-aligned quantity is the energy-based sine "
        r"index \(K_\varepsilon^{H^1,\sin}\) defined in \eqref{eq:Keps_sine_H1}. For comparison, we also "
        r"report the supplementary unweighted sine indices \(K_\varepsilon^{L^2,\sin}\) from "
        r"\eqref{eq:Keps_sine_L2} and the corresponding Chebyshev-based indices. These data are included "
        r"only as numerical illustrations of the same layer-driven reciprocal-Debye trend; they do not "
        r"constitute a verification of the lower-bound theorem.}"
    )
    lines.append(r"\label{tab:scaling_results}")
    lines.append("")
    lines.append(r"\scriptsize")
    lines.append(r"\setlength{\tabcolsep}{2.8pt}")
    lines.append("")
    lines.append(r"\begin{tabular}{cccccccccccc}")
    lines.append(r"\toprule")
    lines.append(
        r"\(\delta\) & \(1/\delta\) & \(\max |u'|\) & clip & "
        r"\(N_{\mathrm{dst}}\) & \(N_{\mathrm{cheb}}\) & "
        r"\(K_{H^1}(10^{-2})\) & \(K_{H^1}(10^{-3})\) & "
        r"\(K_{L^2}(10^{-2})\) & \(K_{L^2}(10^{-3})\) & "
        r"\(K_{\mathrm{cheb}}(10^{-2})\) & \(K_{\mathrm{cheb}}(10^{-3})\) \\"
    )
    lines.append(r"\midrule")

    for r in results:
        delta_str = f"{r.delta:.3f}".rstrip("0").rstrip(".")
        inv_delta_str = f"{int(round(r.inv_delta))}"
        row = (
            f"{delta_str} & {inv_delta_str} & {r.max_abs_uprime:.1f} & {r.clip} & "
            f"{r.N_dst} & {r.N_cheb} & "
            f"\\textbf{{{r.K_H1_1e2}}} & \\textbf{{{r.K_H1_1e3}}} & "
            f"{r.K_L2_1e2} & {r.K_L2_1e3} & "
            f"{r.K_cheb_1e2} & {r.K_cheb_1e3} \\\\"
        )
        lines.append(row)

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append(r"\end{table}")
    return "\n".join(lines)


