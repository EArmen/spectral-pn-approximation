def make_profile_plot(curves, outpath):
    plt.figure(figsize=(7.5, 4.8))
    for item in curves:
        plt.plot(item["s"], item["u"], label=fr"$\delta={item['delta']}$")
    plt.xlabel(r"$s$")
    plt.ylabel(r"$u(s)$")
    plt.title("Equilibrium Poisson--Boltzmann profiles")
    plt.legend()
    plt.tight_layout()
    plt.savefig(outpath, dpi=300, bbox_inches="tight")
    plt.close()


def make_residual_derivative_plot(curves, outpath):
    plt.figure(figsize=(7.5, 4.8))
    for item in curves:
        plt.plot(item["s"], item["wp"], label=fr"$\delta={item['delta']}$")
    plt.xlabel(r"$s$")
    plt.ylabel(r"$w_\delta'(s)$")
    plt.title("Derivative of the residual profile")
    plt.legend()
    plt.tight_layout()
    plt.savefig(outpath, dpi=300, bbox_inches="tight")
    plt.close()


def make_scaling_plot(results, outpath):
    inv_delta = np.array([r.inv_delta for r in results], dtype=float)
    kh1 = np.array([r.K_H1_1e2 for r in results], dtype=float)
    kl2 = np.array([r.K_L2_1e2 for r in results], dtype=float)
    kcheb = np.array([r.K_cheb_1e2 for r in results], dtype=float)

    plt.figure(figsize=(7.5, 4.8))
    plt.plot(inv_delta, kh1, marker="o", label=r"$K_{H^1}(10^{-2})$")
    plt.plot(inv_delta, kl2, marker="s", label=r"$K_{L^2}(10^{-2})$")
    plt.plot(inv_delta, kcheb, marker="^", label=r"$K_{\mathrm{cheb}}(10^{-2})$")
    plt.xlabel(r"$1/\delta = L/L_D$")
    plt.ylabel("Effective spectral dimension")
    plt.title("Observed dependence of effective dimension on reciprocal Debye scale")
    plt.legend()
    plt.tight_layout()
    plt.savefig(outpath, dpi=300, bbox_inches="tight")
    plt.close()


