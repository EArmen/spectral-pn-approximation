results = []
curves = []

for delta in DELTAS:
    print(f"[INFO] Solving delta = {delta}")
    sol, eta = solve_equilibrium_bvp(delta, ALPHA, BETA)

    sampled = evaluate_solution_and_residual(sol, n_points=N_DST)
    s = sampled["s"]
    u = sampled["u"]
    up = sampled["up"]
    w = sampled["w"]
    wp = sampled["wp"]

    print(f"[INFO] Computing sine coefficients for delta = {delta}")
    coeffs_sine = compute_sine_coeffs(w, s, kmax=KMAX_SINE)

    sine_idx = compute_indices_from_sine_coeffs(coeffs_sine, wp, s)

    print(f"[INFO] Computing Chebyshev coefficients for delta = {delta}")
    x_cheb = chebyshev_lobatto_nodes(N_CHEB)
    u_cheb = sol.sol(x_cheb)[0]
    coeffs_cheb = chebyshev_coefficients_from_values_on_lobatto(u_cheb)
    cheb_idx = compute_indices_from_cheb_coeffs(coeffs_cheb)

    result = ExperimentResult(
        delta=delta,
        inv_delta=1.0 / delta,
        max_abs_uprime=float(np.max(np.abs(up))),
        clip=0,
        N_dst=N_DST,
        N_cheb=N_CHEB,
        K_H1_1e2=sine_idx["K_H1_1e2"],
        K_H1_1e3=sine_idx["K_H1_1e3"],
        K_L2_1e2=sine_idx["K_L2_1e2"],
        K_L2_1e3=sine_idx["K_L2_1e3"],
        K_cheb_1e2=cheb_idx["K_cheb_1e2"],
        K_cheb_1e3=cheb_idx["K_cheb_1e3"],
    )

    results.append(result)
    curves.append({
        "delta": delta,
        "s": s,
        "u": u,
        "wp": wp,
    })

results.sort(key=lambda r: r.delta, reverse=True)
curves.sort(key=lambda d: d["delta"], reverse=True)

write_results_csv(results, OUTPUT_DIR / "results.csv")
latex_table = make_latex_table(results)
with open(OUTPUT_DIR / "scaling_table.tex", "w", encoding="utf-8") as f:
    f.write(latex_table)

make_profile_plot(curves, OUTPUT_DIR / "profiles_delta.png")
make_residual_derivative_plot(curves, OUTPUT_DIR / "residual_derivative_delta.png")
make_scaling_plot(results, OUTPUT_DIR / "scaling_plot.png")

print("\n[INFO] Numerical summary")
