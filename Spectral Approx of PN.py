MAX_NODES = 120000
TOL_BVP = 1e-4
EPS_LIST = [1e-2, 1e-3]
BATCH_SIZE = 256

# smoothing width for the step profile in numerics
# small enough to mimic the abrupt junction, large enough for stable solve_bvp
STEP_SMOOTH_FACTOR = 0.08


@dataclass
class ExperimentResult:
    delta: float
    inv_delta: float
    max_abs_uprime: float
    clip: int
    N_dst: int
    N_cheb: int
    K_H1_1e2: int
    K_H1_1e3: int
